    public void addContent(int contentType, int choiceChosen, String userId) {
        int choice = choiceChosen;
        String text= null;
        Image image=null;
        ImageIcon imageIcon= null;
 
        File file= null;
        if (choice == 0) {
            text = JOptionPane.showInputDialog(this, "Please enter your story's text");
            ContentPreviewForUserFrame ipf = new ContentPreviewForUserFrame(this, contentType, null, text);
            ipf.setVisible(true);
        } else if (choice == 1) {
            text = JOptionPane.showInputDialog(this, "Please enter your story's text");
            JFileChooser jfc = new JFileChooser();
            
            jfc.setDialogTitle("Select an Image File");

            jfc.showOpenDialog(this);
            file = jfc.getSelectedFile();
            if(file == null)
            {
                return;
            }
            try {
                image = ImageIO.read(file);
                imageIcon = new ImageIcon(image.getScaledInstance(350, 350, Image.SCALE_SMOOTH));
                ContentPreviewForUserFrame ipf = new ContentPreviewForUserFrame(this, contentType, imageIcon, text);
                ipf.setVisible(true);
            } catch (IOException e) {
                // Handles the exception if the image cannot be loaded
                JOptionPane.showMessageDialog(null, "Failed to load the image: " + e.getMessage());
            }
        }
        
        if (contentType == 1) {
                Posts post= (Posts)ContentFactory.createContent("post", ("C" + (contentIDnum++)) , userId, text);
                if(file!=null){
                post.setImagePath(file.getPath());
                post.setImage(imageIcon);
                }
                contents.add(post);

                user.addPost(post);
                DataBase.getInstance().addToGlobalPosts(post);

            } else if (contentType == 2) {
                Stories story= (Stories)ContentFactory.createContent("story", ("C" + (contentIDnum++)) , userId, text);
                if(file!=null){
                story.setImagePath(file.getPath());
                story.setImage(imageIcon);
                }
                contents.add(story);
                user.addStory(story);
                DataBase.getInstance().addTOGlobalStories(story);

            }
    }

